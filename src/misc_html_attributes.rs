use strum::AsRefStr;

use crate::{Attribute, NumberOrString};

/// An enum of many different html attribute keys, regardless of element type. This can be
/// used in conjunction with [crate::HtmlAttributes] to give you access to practically all HTML
/// attributes. Each variant takes either tuple that represents the valid values for the
/// attributes or nothing to represent a boolean attribute.
#[derive(Debug, AsRefStr)]
#[strum(serialize_all = "lowercase")]
pub enum MiscHtmlAttributes<'a> {
    // Standard HTML Attributes
    Accept(&'a str),
    #[strum(serialize = "accept-charset")]
    AcceptCharset(&'a str),
    Action(&'a str),
    AllowFullScreen(bool),
    AllowTransparency(bool),
    Alt(&'a str),
    As(&'a str),
    Async,
    AutoComplete(&'a str),
    AutoFocus,
    AutoPlay,
    Capture(Capture),
    CellPadding(NumberOrString<'a>),
    CellSpacing(NumberOrString<'a>),
    CharSet(&'a str),
    Challenge(&'a str),
    Checked,
    Cite(&'a str),
    ClassID(&'a str),
    Cols(i64),
    ColSpan(i64),
    Content(&'a str),
    Controls,
    Coords(&'a str),
    CrossOrigin(&'a str),
    Data(&'a str),
    DateTime(&'a str), // TODO: accept Chrono types and convert them to the appropriate strings
    Default,
    Defer,
    Disabled,
    Download,
    EncType(&'a str),
    Form(&'a str),
    FormAction(&'a str),
    FormEncType(&'a str),
    FormMethod(&'a str),
    FormNoValidate,
    FormTarget(&'a str),
    FrameBorder(FrameBorder),
    Headers(&'a str),
    Height(NumberOrString<'a>),
    High(i64),
    Href(&'a str),
    HrefLang(&'a str),
    For(&'a str),
    HttpEquiv(&'a str),
    Integrity(&'a str),
    KeyParams(&'a str),
    KeyType(&'a str),
    Kind(&'a str),
    Label(&'a str),
    List(&'a str),
    Loop,
    Low(i64),
    Manifest(&'a str),
    MarginHeight(i64),
    MarginWidth(i64),
    Max(NumberOrString<'a>),
    MaxLength(i64),
    Media(&'a str),
    MediaGroup(&'a str),
    Method(&'a str),
    Min(NumberOrString<'a>),
    MinLength(i64),
    Multiple,
    Muted,
    Name(&'a str),
    Nonce(&'a str),
    NoValidate,
    Open,
    Optimum(i64),
    Pattern(&'a str),
    Placeholder(&'a str),
    PlaysInline,
    Poster(&'a str),
    Preload(&'a str),
    ReadOnly,
    Rel(&'a str),
    Required,
    Reversed,
    Rows(i64),
    RowSpan(i64),
    Sandbox(&'a str),
    Scope(&'a str),
    Scoped,
    Scrolling(&'a str),
    Seamless,
    Selected,
    Shape(&'a str),
    Size(i64),
    Sizes(&'a str),
    Span(i64),
    Src(&'a str),
    SrcDoc(&'a str),
    SrcLang(&'a str),
    SrcSet(&'a str),
    Start(i64),
    Step(NumberOrString<'a>),
    Summary(&'a str),
    Target(&'a str),
    Type(&'a str),
    UseMap(&'a str),
    Value(&'a str),
    Width(NumberOrString<'a>),
    Wwmode(&'a str),
    Wrap(&'a str),
}

